DROP PROCEDURE IF EXISTS CheckPraktikums;
DELIMITER //
CREATE PROCEDURE CheckPraktikums(
    IN in_kelas_kode CHAR(10),
    OUT prak_tglPraktikum TIMESTAMP,
    OUT mk_nama VARCHAR(50),
    OUT prak_judul VARCHAR(50),
    OUT ruang_kode CHAR(5)
)
BEGIN
    SELECT P.prak_tglPraktikum , Mk.mk_nama, P.prak_judul, P.ruang_kode
    INTO prak_tglPraktikum, mk_nama, prak_judul, ruang_kode
    FROM KELAS AS K
    JOIN PRAKTIKAN_MENGAMBIL_KELAS PM ON PM.kelas_kode = K.kelas_kode
    JOIN praktikum P ON P.kelas_kode = K.kelas_kode
    JOIN MATA_KULIAH MK ON MK.mk_kode = K.mk_kode
    WHERE in_kelas_kode = PM.kelas_kode
    ORDER BY P.prak_tglPraktikum ASC
    LIMIT 1;
END//
DELIMITER ;

