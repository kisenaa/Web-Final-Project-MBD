CREATE TABLE PRAKTIKAN(
    mhs_nrp CHAR(10) PRIMARY KEY,
    mhs_nama VARCHAR(100),
    mhs_angkatan INTEGER
);

CREATE TABLE ASDOS(
    asdos_kode CHAR(6) PRIMARY KEY,
    asdos_nama VARCHAR(100),
    asdos_nrp CHAR(10),
    asdos_angkatan INTEGER,
    asdos_noTelp VARCHAR(12)
);

CREATE TABLE MATA_KULIAH(
    mk_kode CHAR(5) PRIMARY KEY,
    mk_nama VARCHAR(50),
    mk_sks INTEGER,
    mk_semester INTEGER
);

CREATE TABLE KELAS(
    kelas_kode CHAR(6) PRIMARY KEY,
    kelas_nama CHAR(1),
    kelas_jadwal TIME,
    mk_kode CHAR(5),
    FOREIGN KEY (mk_kode) REFERENCES MATA_KULIAH(mk_kode)
);

CREATE TABLE ASDOS_MEMBIMBING_KELAS(
    asdos_kode CHAR(6),
    kelas_kode CHAR(6),
    PRIMARY KEY(asdos_kode, kelas_kode),
    FOREIGN KEY (asdos_kode) REFERENCES ASDOS(asdos_kode),
    FOREIGN KEY (kelas_kode) REFERENCES KELAS(kelas_kode)
);

CREATE TABLE PRAKTIKAN_MENGAMBIL_KELAS(
    mhs_nrp CHAR(10),
    kelas_kode CHAR(6),
    PRIMARY KEY(mhs_nrp, kelas_kode),
    FOREIGN KEY (mhs_nrp) REFERENCES PRAKTIKAN(mhs_nrp),
    FOREIGN KEY (kelas_kode) REFERENCES KELAS(kelas_kode)
);

CREATE TABLE RUANGAN(
    ruang_kode CHAR(5) PRIMARY KEY,
    ruang_nama VARCHAR(50),
    ruang_kapasitas INTEGER
);

CREATE TABLE PRAKTIKUM(
    prak_kode CHAR(7),
    prak_judul VARCHAR(50),
    prak_tglPraktikum TIMESTAMP,
    ruang_kode CHAR(5),
    kelas_kode CHAR(6),
    FOREIGN KEY (kelas_kode) REFERENCES KELAS(kelas_kode),
    FOREIGN KEY (ruang_kode) REFERENCES RUANGAN(ruang_kode)
);

